    private void btn_modifierActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_modifierActionPerformed
        EncadreurDao encdao = new EncadreurDao();
        int index = list_encadreur.getSelectedRow();
        SimpleDateFormat dateToString = new SimpleDateFormat("yyyyHHmmss");
        Date date = new Date();
        String date_created = dateToString.format(date);
        String matricule = "";
        String nom = txt_nom.getText();
        String prenom = txt_prenom.getText();
        String sexe = txt_sexe.getSelectedItem().toString();
        String tel = txt_tel.getText();
        String adresse = txt_adresse.getText();
        String email = txt_email.getText();
        String statut = txt_statut.getSelectedItem().toString();
        String matricul = txt_matricule.getText();

        if (prenom.equals("") || nom.equals("") || sexe.equals("") || tel.equals("") || adresse.equals("") || email.equals("") || statut.equals("")) {
            JOptionPane.showMessageDialog(this, "Reseigner les champs", "Erreur", JOptionPane.ERROR_MESSAGE);
        } else {
            String mat = nom.substring(0, 2) + "" + prenom.substring(0, 2) + "" + date_created.substring(0, 10);

            if (matricul == mat) {
                matricule = matricul;
            } else {
                matricule = mat;
            }
            long id = (long) list_encadreur.getValueAt(index, 0);
            Encadreur enc = new Encadreur(matricule.toUpperCase(), nom, prenom, sexe, tel, adresse, email, statut);
            encdao.edit(enc, id);
            vider_enc();
            list_enc();
            JOptionPane.showMessageDialog(this, "Reseigner les champs");
        }
    }//GEN-LAST:event_btn_modifierActionPerformed

