    private void vider_enc() {
        txt_prenom.setText("");
        txt_nom.setText("");
        txt_statut.setSelectedItem("");
        txt_tel.setText("");
        txt_adresse.setText("");
        txt_sexe.setSelectedItem("");
        txt_email.setText("");
        txt_matricule.setText("");
        DefaultTableModel model = (DefaultTableModel) list_encadreur.getModel();
        int n = model.getRowCount();
        for (int i = n - 1; i >= 0; i--) {
            model.removeRow(i);
        }
    }

    private void list_enc() {
        EncadreurDao encdao = new EncadreurDao();
        List<Encadreur> enc = encdao.liste();
        int i;
        for (i = 0; i < enc.size(); i++) {
            ((DefaultTableModel) list_encadreur.getModel()).addRow(new Object[]{
                enc.get(i).getId(),
                i + 1,
                enc.get(i).getMatricule(),
                enc.get(i).getNom(),
                enc.get(i).getPrenom(),
                enc.get(i).getSexe(),
                enc.get(i).getTel(),
                enc.get(i).getAdresse(),
                enc.get(i).getEmail(),
                enc.get(i).getStatut(),});
        }
    }

