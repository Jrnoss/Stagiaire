    private void btn_save2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_save2ActionPerformed
        EncadreurDao encdao = new EncadreurDao();
        SimpleDateFormat dateToString = new SimpleDateFormat("yyyyHHmmss");
        Date date = new Date();
        String date_created = dateToString.format(date);
        String nom = txt_nom.getText();
        String prenom = txt_prenom.getText();
        String sexe = txt_sexe.getSelectedItem().toString();
        String tel = txt_tel.getText();
        String adresse = txt_adresse.getText();
        String email = txt_email.getText();
        String statut = txt_statut.getSelectedItem().toString();
//    String matricule = txt_matricule.getText();
        if (nom.equals("") || prenom.equals("") || sexe.equals("") || tel.equals("") || adresse.equals("") || email.equals("") || statut.equals("")) {
            JOptionPane.showMessageDialog(this, "Renseigner les champs Svp");
        } else {
            String matricule = nom.substring(0, 2) + "" + prenom.substring(0, 2) + "" + date_created.substring(0, 10);

            Encadreur enc = new Encadreur(matricule.toUpperCase(), nom, prenom, sexe, tel, adresse, email, statut);
            encdao.add(enc);
            vider_enc();
            list_enc();
            JOptionPane.showMessageDialog(this, "Enregistrer avec succes");
        }
    }//GEN-LAST:event_btn_save2ActionPerformed

